const State = {
	/* =================================================
	   DEFAULT
	================================================= */

	defaultForm() {
		return {
			id: null,

			name: '',

			phone: '',

			email: '',

			address: '',

			description: '',
		};
	},

	/* =================================================
	   FORM
	================================================= */

	form: {},

	/* =================================================
	   STATUS
	================================================= */

	loading: false,

	submitting: false,

	/* =================================================
	   SET SUPPLIER
	================================================= */

	setSupplier(supplier = {}) {
		this.form = {
			...this.form,

			...supplier,
		};
	},

	/* =================================================
	   SET FIELD
	================================================= */

	setField(key, value) {
		if (key in this.form) {
			this.form[key] = value;
		}
	},

	/* =================================================
	   RESET
	================================================= */

	reset() {
		this.form = this.defaultForm();
	},
};

State.reset();

export default State;
